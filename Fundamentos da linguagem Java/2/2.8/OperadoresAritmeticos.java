public class OperadoresAritmeticos {

   public static void main(String[] args) {
	int minhaIdade = 35;
	int suaIdade = 30;
	
	//Adição
	int totalIdades = minhaIdade + suaIdade;
	System.out.println("Soma das idades: " + totalIdades);
	
	
	//Subtração
	int diferencaEntreAsIdades = minhaIdade - suaIdade;
	System.out.println("Diferença entre as idades: " + diferencaEntreAsIdades);
	
	
	//Multiplicação
	int dobroDaIdade = 2 * suaIdade;
	System.out.println("O Dobro da sua idade: " + dobroDaIdade);
	
	
	//Divisão
	int metadeDaIdade = suaIdade/2;
	System.out.println("Metade da sua idade: " + metadeDaIdade);
	
	
	//Módulo
	int restoDaDivisao = 7 % 2;
	System.out.println("Resto da divisão (módulo): " + restoDaDivisao);
	
    }
	
}

